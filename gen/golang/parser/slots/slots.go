package slots

import (
	"bytes"
	"text/template"

	"github.com/goccmack/gogll/ast"
	"github.com/goccmack/gogll/frstflw"
	"github.com/goccmack/gogll/gslot"
	"github.com/goccmack/goutil/ioutil"
)

func Gen(slotFile string, g *ast.GoGLL, gs *gslot.GSlot, ff *frstflw.FF) {
	tmpl, err := template.New("Slot").Parse(slotTmpl)
	if err != nil {
		panic(err)
	}
	buf, data := new(bytes.Buffer), getData(g, gs, ff)
	if err = tmpl.Execute(buf, data); err != nil {
		panic(err)
	}
	if err := ioutil.WriteFile(slotFile, buf.Bytes()); err != nil {
		panic(err)
	}
}

type Data struct {
	Package string
	Slots   []*SlotData
	Alts    []*AltData
}

type AltData struct {
	NT     string
	Labels string
}

type SlotData struct {
	Label   string
	NT      string
	Alt     int
	Pos     int
	Symbols []string
}

func getData(g *ast.GoGLL, gs *gslot.GSlot, ff *frstflw.FF) *Data {
	return &Data{
		Package: g.Package.GetString(),
		Slots:   getSlotData(gs),
		Alts:    getAltData(g, gs, ff),
	}
}

func getAltData(g *ast.GoGLL, gs *gslot.GSlot, ff *frstflw.FF) (data []*AltData) {
	for _, r := range g.Rules {
		d := &AltData{r.Head.Token(), getLabelList(r, g, gs, ff)}
		data = append(data, d)
	}
	return
}

func getLabelList(rule *ast.Rule, g *ast.GoGLL, gs *gslot.GSlot, ff *frstflw.FF) string {
	buf := new(bytes.Buffer)
	for i := range rule.Alternates {
		if i > 0 {
			buf.WriteString(",")
		}
		buf.WriteString(gslot.NewLabel(rule.Head.Token(), i, 0, gs, ff).Label())
	}
	return buf.String()
}

func getSlotData(gs *gslot.GSlot) (data []*SlotData) {
	for _, s := range gs.Slots() {
		d := &SlotData{
			Label:   s.Label(),
			NT:      s.Head,
			Alt:     s.Alternate,
			Pos:     s.Pos,
			Symbols: s.Symbols().Strings(),
		}
		data = append(data, d)
	}
	return
}

